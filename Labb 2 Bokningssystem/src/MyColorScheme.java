import java.awt.Color;

public class MyColorScheme {
	private Color color;
	
	public Color lightBlue() {
		return color = new Color(206, 235, 251);
	}
	public Color mediumBlue() {
		return color = new Color(163, 214, 245);
	}
	public Color blue() {
		return color = new Color(0, 114, 178);
	}
	public Color red() {
		return color = new Color(238, 50, 51);
	}
	public Color lavender() {
		return color = new Color(240, 236, 235);
	}
	public Color grey() {
		return color = new Color(108, 116, 118);
	}
	public Color black() {
		return color = new Color(0, 0, 0);
	}
	public Color green() {
		return color = new Color(23, 230, 23);
	}
	public Color darkLavender() {
		return color = new Color(210, 206, 205);
	}
}
